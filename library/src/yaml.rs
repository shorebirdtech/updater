/// Controls when patch signature verification occurs.
#[derive(Clone, Copy, Debug, Default, PartialEq, Eq)]
pub enum PatchVerificationMode {
    /// Verify patch signature at boot time (default, most secure).
    /// The patch is verified each time the app boots, ensuring
    /// the patch file hasn't been tampered with since installation.
    #[default]
    Strict,
    /// Verify patch signature only at install time.
    /// Faster boot times but less protection against post-install tampering.
    InstallOnly,
}

/// Struct for parsing shorebird.yaml.
#[derive(Debug)]
pub struct YamlConfig {
    /// App ID.  Required.  Generated by Shorebird and included
    /// in your app to identify which app/channel/version triple to update.
    pub app_id: String,
    /// Update channel name.  Defaults to "stable" if not set.
    pub channel: Option<String>,
    /// Update URL.  Defaults to the default update URL if not set.
    pub base_url: Option<String>,
    /// Update behavior. Defaults to true if not set.
    pub auto_update: Option<bool>,
    /// Base64-encoded public key for verifying patch hash signatures.
    pub patch_public_key: Option<String>,
    /// When to verify patch signatures. Defaults to "strict" (verify at boot time).
    pub patch_verification: Option<PatchVerificationMode>,
}

impl YamlConfig {
    /// Read in shorebird.yaml from a string.
    ///
    /// This is a minimal parser for simple key-value YAML (no nesting,
    /// no flow syntax, no anchors). This is sufficient for shorebird.yaml
    /// which we fully control.
    pub fn from_yaml(yaml: &str) -> Result<Self, String> {
        let mut app_id: Option<String> = None;
        let mut channel: Option<String> = None;
        let mut base_url: Option<String> = None;
        let mut auto_update: Option<bool> = None;
        let mut patch_public_key: Option<String> = None;
        let mut patch_verification: Option<PatchVerificationMode> = None;

        for line in yaml.lines() {
            let line = line.trim();
            if line.is_empty() || line.starts_with('#') {
                continue;
            }

            let Some((key, value)) = line.split_once(':') else {
                continue;
            };
            let key = key.trim();
            let value = value.trim();

            match key {
                "app_id" => app_id = Some(value.to_string()),
                "channel" => channel = Some(value.to_string()),
                "base_url" => base_url = Some(value.to_string()),
                "auto_update" => {
                    auto_update =
                        Some(value.parse::<bool>().map_err(|e| {
                            format!("invalid value for auto_update: '{value}': {e}")
                        })?);
                }
                "patch_public_key" => patch_public_key = Some(value.to_string()),
                "patch_verification" => {
                    patch_verification = Some(match value {
                        "strict" => PatchVerificationMode::Strict,
                        "install_only" => PatchVerificationMode::InstallOnly,
                        _ => {
                            return Err(format!("invalid value for patch_verification: '{value}'"))
                        }
                    });
                }
                _ => {} // Ignore unknown keys for forward compatibility.
            }
        }

        let app_id = app_id.ok_or("missing required field: app_id")?;

        Ok(YamlConfig {
            app_id,
            channel,
            base_url,
            auto_update,
            patch_public_key,
            patch_verification,
        })
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn parses_patch_verification_strict() {
        let yaml = r#"
app_id: test_app
patch_verification: strict
"#;
        let config = YamlConfig::from_yaml(yaml).unwrap();
        assert_eq!(
            config.patch_verification,
            Some(PatchVerificationMode::Strict)
        );
    }

    #[test]
    fn parses_patch_verification_install_only() {
        let yaml = r#"
app_id: test_app
patch_verification: install_only
"#;
        let config = YamlConfig::from_yaml(yaml).unwrap();
        assert_eq!(
            config.patch_verification,
            Some(PatchVerificationMode::InstallOnly)
        );
    }

    #[test]
    fn defaults_to_none_when_not_specified() {
        let yaml = r#"
app_id: test_app
"#;
        let config = YamlConfig::from_yaml(yaml).unwrap();
        assert_eq!(config.patch_verification, None);
        // When unwrapped with default, should be Strict
        assert_eq!(
            config.patch_verification.unwrap_or_default(),
            PatchVerificationMode::Strict
        );
    }

    #[test]
    fn default_verification_mode_is_strict() {
        assert_eq!(
            PatchVerificationMode::default(),
            PatchVerificationMode::Strict
        );
    }

    #[test]
    fn missing_app_id_is_error() {
        let yaml = "channel: stable\n";
        let result = YamlConfig::from_yaml(yaml);
        assert!(result.is_err());
        assert!(result.unwrap_err().contains("app_id"));
    }

    #[test]
    fn parses_all_fields() {
        let yaml = r#"
app_id: my_app
channel: beta
base_url: https://example.com
auto_update: false
patch_public_key: abc123
patch_verification: install_only
"#;
        let config = YamlConfig::from_yaml(yaml).unwrap();
        assert_eq!(config.app_id, "my_app");
        assert_eq!(config.channel.as_deref(), Some("beta"));
        assert_eq!(config.base_url.as_deref(), Some("https://example.com"));
        assert_eq!(config.auto_update, Some(false));
        assert_eq!(config.patch_public_key.as_deref(), Some("abc123"));
        assert_eq!(
            config.patch_verification,
            Some(PatchVerificationMode::InstallOnly)
        );
    }

    #[test]
    fn ignores_comments_and_blank_lines() {
        let yaml = r#"
# This is a comment
app_id: test_app

# Another comment
channel: stable
"#;
        let config = YamlConfig::from_yaml(yaml).unwrap();
        assert_eq!(config.app_id, "test_app");
        assert_eq!(config.channel.as_deref(), Some("stable"));
    }

    #[test]
    fn ignores_unknown_keys() {
        let yaml = r#"
app_id: test_app
unknown_key: some_value
"#;
        let config = YamlConfig::from_yaml(yaml).unwrap();
        assert_eq!(config.app_id, "test_app");
    }

    #[test]
    fn invalid_auto_update_is_error() {
        let yaml = r#"
app_id: test_app
auto_update: maybe
"#;
        let result = YamlConfig::from_yaml(yaml);
        assert!(result.is_err());
        assert!(result.unwrap_err().contains("auto_update"));
    }

    #[test]
    fn fields_in_any_order() {
        let yaml = r#"
patch_verification: strict
base_url: https://example.com
app_id: my_app
auto_update: true
channel: beta
patch_public_key: abc123
"#;
        let config = YamlConfig::from_yaml(yaml).unwrap();
        assert_eq!(config.app_id, "my_app");
        assert_eq!(config.channel.as_deref(), Some("beta"));
        assert_eq!(config.base_url.as_deref(), Some("https://example.com"));
        assert_eq!(config.auto_update, Some(true));
        assert_eq!(config.patch_public_key.as_deref(), Some("abc123"));
        assert_eq!(
            config.patch_verification,
            Some(PatchVerificationMode::Strict)
        );
    }

    #[test]
    fn invalid_patch_verification_is_error() {
        let yaml = r#"
app_id: test_app
patch_verification: unknown_mode
"#;
        let result = YamlConfig::from_yaml(yaml);
        assert!(result.is_err());
        assert!(result.unwrap_err().contains("patch_verification"));
    }
}
